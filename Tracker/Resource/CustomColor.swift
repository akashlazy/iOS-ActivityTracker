    class func colorGyroscope(_ index: Int) -> UIColor {
        if index == 0 {
            return UIColor(rgba: "#D8433CFF")
        } else if index == 1 {
            return UIColor(rgba: "#F2AF3AFF")
        } else if index == 2 {
            return UIColor(rgba: "#3F8AF8FF")
        } else if index == 3 {
            return UIColor(rgba: "#279B5EFF")
        } else {
            return UIColor(rgba: "#D8433CFF")
        }
    }
